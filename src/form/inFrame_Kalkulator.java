    private void btEqualsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEqualsActionPerformed
        String jawab;
        nomKedua = Double.parseDouble(txField_Input.getText());
        if (operator == "+") {
            hasil = nomPertama + nomKedua;
            jawab = String.format("%.0f", hasil);
            txField_Input.setText(jawab);
        } else if (operator == "-") {
            hasil = nomPertama - nomKedua;
            jawab = String.format("%.0f", hasil);
            txField_Input.setText(jawab);
        } else if (operator == "*") {
            hasil = nomPertama * nomKedua;
            jawab = String.format("%.0f", hasil);
            txField_Input.setText(jawab);
        } else if (operator == "/") {
            hasil = nomPertama / nomKedua;
            jawab = String.format("%.0f", hasil);
            txField_Input.setText(jawab);
        } else if (operator == "%") {
            hasil = nomPertama % nomKedua;
            jawab = String.format("%.0f", hasil);
            txField_Input.setText(jawab);
        }
    }//GEN-LAST:event_btEqualsActionPerformed

